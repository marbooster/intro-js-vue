        this.formData.dateSelect.year = 1000000000000000
      }
      let age = date.getFullYear() - (+this.formData.dateSelect.year);
      if (
        this.formData.dateSelect.day === "" ||
        this.formData.dateSelect.month === "" ||
        age < 10
      ) {
        this.dateValidation.error = true;
        this.dateValidation.success = false;
        this.dateValidation.message = "Entrer une date valide";
      } else {
        this.dateValidation.success = true;
        this.dateValidation.error = false;
      }
    },
    validateInputs() {
      let prenomValue = this.formData.prenom.trim();
      let nomValue = this.formData.nom.trim();
      let emailN = this.formData.email.trim();
      let numberValue = this.formData.particip;
