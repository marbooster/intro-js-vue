      if (prenomValue === "") {
        this.prenomValidation.error = true;
        this.prenomValidation.message = "Le prenom est requis";
      } else if (!prenomValue.match(letters)) {
        this.prenomValidation.error = true;
        this.prenomValidation.message =
          "Entrer des lettres. les chiffres ne sont pas autorisés";
      } else if (prenomValue.length < 3) {
        this.prenomValidation.error = true;
        this.prenomValidation.message = "Le prenom est requis";
      } else {
        this.prenomValidation.error = false;
        this.prenomValidation.success = true;
        this.formData.prenom = this.formData.prenom.replace(
          /^.{1}/g,
          this.formData.prenom[0].toUpperCase()
        );
      }
      if (nomValue === "") {
        this.nomValidation.error = true;
        this.nomValidation.message = "Le nom est requis";
      } else if (!nomValue.match(letters)) {
        this.nomValidation.error = true;
        this.nomValidation.message =
          "Entrer des lettres. les chiffres ne sont pas autorisés";
      } else if (nomValue.length < 3) {
        this.nomValidation.error = true;
        this.nomValidation.message = "Le nom est requis";
      } else {
        this.nomValidation.error = false;
        this.nomValidation.success = true;
        this.formData.nom = this.formData.nom.replace(
          /^.{1}/g,
          this.formData.nom[0].toUpperCase()
        );
      }
      if (emailN === "") {
        this.emailValidation.error = true;
        this.emailValidation.message = "L'email est requis";
      } else if (!this.isValidEmail(emailN)) {
        this.emailValidation.error = true;
        this.emailValidation.message = "Adresse Email Invalide";
      } else {
        this.emailValidation.error = false;
        this.emailValidation.success = true;
      }
      if (numberValue === null || numberValue === undefined) {
        this.numberValidation.error = true;
        this.numberValidation.message = "Le champ est réquis";
      } else if (isNaN(numberValue)) {
        this.numberValidation.error = true;
        this.numberValidation.message = "Entrer que des nombres";
      } else {
        this.numberValidation.error = false;
        this.numberValidation.success = true;
      }
      this.selectYear();
